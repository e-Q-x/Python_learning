#однострочный комментарий
"""
Это многострочный
комментарий
целочисленный тип integer
вещественный тим float
"""
a = float(input('введите первое число:'))
b = float(input('введите второе число:'))
print(f'{a} + {b} = {a + b}')
print(f'{a} - {b} = {a - b}')
print(f'{a} * {b} = {a * b}')
print(f'{a} / {b} = {a / b}')